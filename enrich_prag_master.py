def get_google_photo_url(place_name):
    try:
        # 1. Find Place ID and Photo Reference
        search_url = "https://maps.googleapis.com/maps/api/place/findplacefromtext/json"
        params = {
            "input": f"{place_name} Prague",
            "inputtype": "textquery",
            "fields": "photos,name,geometry",
            "key": API_KEY
        }
        response = requests.get(search_url, params=params)
        data = response.json()
        
        if data.get("candidates") and len(data["candidates"]) > 0:
            candidate = data["candidates"][0]
            if "photos" in candidate:
                photo_ref = candidate["photos"][0]["photo_reference"]
                return f"https://maps.googleapis.com/maps/api/place/photo?maxwidth=800&photo_reference={photo_ref}&key={API_KEY}"
            else:
                print(f"⚠️ No photos found for: {place_name}")
        else:
            print(f"❌ Place not found: {place_name}")
            
    except Exception as e:
        print(f"Error fetching image for {place_name}: {e}")
    
    return ""


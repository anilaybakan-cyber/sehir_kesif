def enrich_midilli_master():
    filepath = 'assets/cities/midilli.json'
    all_new_places = new_midilli_places_1 + new_midilli_places_2 + new_midilli_places_3 + new_midilli_places_4 + new_midilli_places_5 + new_midilli_places_6 + new_midilli_places_7 + new_midilli_places_8 + new_midilli_places_9 + new_midilli_places_10 + new_midilli_places_11 + new_midilli_places_12 + new_midilli_places_13 + new_midilli_places_14 + new_midilli_places_15 + new_midilli_places_16 + new_midilli_places_17 + new_midilli_places_18 + new_midilli_places_19 + new_midilli_places_20 + new_midilli_places_21
    
    # Load existing data
    try:
        with open(filepath, 'r', encoding='utf-8') as f:
            data = json.load(f)
    except FileNotFoundError:
        print(f"Error: {filepath} not found.")
        return

    existing_names = {place['name'].lower() for place in data.get('highlights', [])}
    
    print(f"Adding {len(all_new_places)} new places to Midilli...")
    
    added_count = 0
    for place in all_new_places:
        if place['name'].lower() in existing_names:
            print(f"Skipping existing: {place['name']}")
            continue
            
        # 1. Generate ID
        place['id'] = place['name'].lower().replace(' ', '-').replace('ç', 'c').replace('ğ', 'g').replace('ı', 'i').replace('ö', 'o').replace('ş', 's').replace('ü', 'u')
        
        # 2. Add missing fields
        if 'subcategory' not in place:
            place['subcategory'] = place['category']
        if 'bestFor' not in place:
            place['bestFor'] = ["herkes"]
            
        # 3. Fetch Image from Google Places
        photo_url = get_google_photo_url(place['name_en'], location_bias="Lesbos")
        if photo_url:
            place['imageUrl'] = photo_url
            place['source'] = 'google'
        else:
             # Fallback or keep empty (handled by app)
            place['imageUrl'] = ""
            place['source'] = 'manual'

        data['highlights'].append(place)
        added_count += 1
        
    # Save updated data
    with open(filepath, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
        
    print(f"Successfully added {added_count} new places to {filepath}.")

if __name__ == "__main__":
    enrich_midilli_master()

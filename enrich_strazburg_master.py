def get_google_photo_url(place_name):
    """
    Fetches a photo URL from Google Places API for a given place name.
    """
    try:
        # 1. Find Place ID
        find_url = f"https://maps.googleapis.com/maps/api/place/findplacefromtext/json?input={urllib.parse.quote(place_name)}&inputtype=textquery&fields=photos,place_id&key={GOOGLE_API_KEY}"
        resp = requests.get(find_url)
        data = resp.json()
        
        if data['status'] == 'OK' and data['candidates']:
            candidate = data['candidates'][0]
            if 'photos' in candidate:
                photo_reference = candidate['photos'][0]['photo_reference']
                # 2. Build Photo URL
                photo_url = f"https://maps.googleapis.com/maps/api/place/photo?maxwidth=800&photoreference={photo_reference}&key={GOOGLE_API_KEY}"
                print(f"  Found photo for: {place_name}")
                return photo_url
            else:
                print(f"  No photos found for: {place_name}")
        else:
            print(f"  Place not found: {place_name}")
            
    except Exception as e:
        print(f"  Error fetching photo for {place_name}: {e}")
    
    return None


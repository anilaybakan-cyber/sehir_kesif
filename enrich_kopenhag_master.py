def get_google_photo_url(place_name, city="Copenhagen"):
    """
    Fetches the first photo URL for a place using Google Places API.
    """
    try:
        # 1. Search for the place to get Place ID
        search_url = "https://maps.googleapis.com/maps/api/place/findplacefromtext/json"
        params = {
            "input": f"{place_name} {city}",
            "inputtype": "textquery",
            "fields": "place_id,photos",
            "key": API_KEY
        }
        response = requests.get(search_url, params=params)
        data = response.json()

        if "candidates" in data and len(data["candidates"]) > 0:
            candidate = data["candidates"][0]
            if "photos" in candidate:
                photo_reference = candidate["photos"][0]["photo_reference"]
                # 2. Construct Photo URL
                photo_url = f"https://maps.googleapis.com/maps/api/place/photo?maxwidth=800&photoreference={photo_reference}&key={API_KEY}"
                print(f"  Found photo for: {place_name}")
                return photo_url
            else:
                print(f"  No photos found for: {place_name}")
                return None
        else:
            print(f"  Place not found: {place_name}")
            return None

    except Exception as e:
        print(f"  Error fetching photo for {place_name}: {e}")
        return None


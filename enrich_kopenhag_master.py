def enrich_kopenhag_master():
    filepath = 'assets/cities/kopenhag.json'
    all_new_places = new_kopenhag_places_1 + new_kopenhag_places_2 + new_kopenhag_places_3 + new_kopenhag_places_4 + new_kopenhag_places_5 + new_kopenhag_places_6 + new_kopenhag_places_7 + new_kopenhag_places_8 + new_kopenhag_places_9 + new_kopenhag_places_10 + new_kopenhag_places_11 + new_kopenhag_places_12 + new_kopenhag_places_13 + new_kopenhag_places_14 + new_kopenhag_places_15 + new_kopenhag_places_16 + new_kopenhag_places_17 + new_kopenhag_places_18 + new_kopenhag_places_19 + new_kopenhag_places_20 + new_kopenhag_places_21
    
    # Load existing data
    try:
        with open(filepath, 'r', encoding='utf-8') as f:
            city_data = json.load(f)
    except FileNotFoundError:
        print(f"Error: {filepath} not found.")
        return

    existing_names = {p['name'].lower() for p in city_data.get('highlights', [])}
    
    print(f"Adding {len(all_new_places)} new places to {city_data['city']}...")

    added_count = 0
    for place in all_new_places:
        if place['name'].lower() in existing_names:
            print(f"Skipping existing: {place['name']}")
            continue

        # 1. Fetch Photo from Google Places API
        print(f"Fetching photo for: {place['name']}...")
        photo_url = get_google_photo_url(place['name'], city_data['city'])
        
        if photo_url:
             place['imageUrl'] = photo_url
             place['source'] = 'google'
        else:
            # Fallback if no photo found (keep it empty or use a default if you have one)
            # For now, we'll leave it without imageUrl or use a placeholder if truly needed.
            # But the goal is Google Images.
            place['imageUrl'] = "" 
            place['source'] = 'google_failed'

        # 2. Generate ID
        place['id'] = place['name'].lower().replace(' ', '-').replace('ö', 'o').replace('ä', 'a').replace('å', 'a').replace('ø', 'o').replace('æ', 'ae')
        
        # 3. Add to list
        city_data['highlights'].append(place)
        added_count += 1
        
        # Rate limit
        time.sleep(1)

    # Save back
    with open(filepath, 'w', encoding='utf-8') as f:
        json.dump(city_data, f, ensure_ascii=False, indent=2)

    print(f"Successfully added {added_count} new places to {filepath}.")

if __name__ == "__main__":
    enrich_kopenhag_master()

        highlights = data.get('highlights', [])
        existing_names = {h['name'].lower() for h in highlights}
        
        added_count = 0
        print(f"Adding new places to Stockholm... (Current count: {len(highlights)})")
        
        for place in all_new_places:
            if place['name'].lower() in existing_names:
                print(f"Skipping existing: {place['name']}")
                continue
                
            print(f"Fetching photo for: {place['name']}...")
            photo_url = get_google_photo_url(place['name'])
